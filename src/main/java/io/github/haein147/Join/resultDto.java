package io.github.haein147.Join;

public class resultDto {
	private String title;
	private String description;
	private Double score;
	private Double descriptionsize;
	private Double pageview;
	private Double editcount;

	public Double getDescriptionsize() {
		return descriptionsize;
	}
	public void setDescriptionsize(Double descriptionsize) {
		this.descriptionsize = descriptionsize;
	}
	public Double getPageview() {
		return pageview;
	}
	public void setPageview(Double pageview) {
		this.pageview = pageview;
	}
	public Double getEditcount() {
		return editcount;
	}
	public void setEditcount(Double editcount) {
		this.editcount = editcount;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public Double getScore() {
		return score;
	}
	public void setScore(Double score) {
		this.score = score;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
}
